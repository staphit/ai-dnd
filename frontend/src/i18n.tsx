interface I18nValue {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: MessageKey) => string;
  /** Translate a literal zh-TW string; falls back to the original when unlisted or lang is zh. */
  tz: (text: string) => string;
}

const I18nContext = createContext<I18nValue>({
  lang: 'zh',
  setLang: () => {},
  t: (key) => messages[key].zh,
  tz: (text) => text,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => {
    try {
      return window.localStorage.getItem(STORAGE_KEY) === 'en' ? 'en' : 'zh';
    } catch {
      return 'zh';
    }
  });

  useEffect(() => {
    document.documentElement.lang = lang === 'en' ? 'en' : 'zh-Hant';
  }, [lang]);

  function setLang(next: Language) {
    setLangState(next);
    try {
      window.localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // Private-mode storage failures just lose persistence, not the toggle.
    }
  }

  const t = (key: MessageKey) => messages[key][lang];
  const tz = (text: string) => (lang === 'en' ? zhToEn[text] ?? text : text);
  return <I18nContext.Provider value={{ lang, setLang, t, tz }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  return useContext(I18nContext);
}
